    private void jButton7MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton7MouseClicked
        // TODO add your handling code here:
        if(jList1.getSelectedIndex()>=0)
        {
            if(selected_Swimmers_List.size()<=4)
            {
                selected_Swimmers_List_model.add(selected_Swimmers_List_model.size(), jList1.getSelectedValue().toString());
                selected_Swimmers_List.add(loaded_Swimmers_List.get(jList1.getSelectedIndex()));

                loaded_Swimmers_List.remove(jList1.getSelectedIndex());
                loaded_Swimmers_List_model.remove(jList1.getSelectedIndex());

                if(selected_Swimmers_List.size()>0) 
                { jButton4.setEnabled(true);}
                else
                { jButton4.setEnabled(false);}
            }
            else
            {
                Message_Box.errorBox("You can select only 5 Swimmers \n Because we have only 5 Swim Lines.","Over Selection",this);
            }
        }
        else
        {
            Message_Box.errorBox("Please Select a Swimmer to move","Empty Selection",this);
        }
        
    }//GEN-LAST:event_jButton7MouseClicked

