    private void Load_Swimmes_Datas(int gender_id,int stroke_id,int length_id)
    {
        jButton4.setEnabled(false);         // next Button
        
        loaded_Swimmers_List_model.clear();
        loaded_Swimmers_List.clear();
        selected_Swimmers_List_model.clear();
        selected_Swimmers_List.clear();
        
        int index=0;
        for(String item:Swimmers_List)
        {
            String[] swimmer_item=item.split(",");
            //System.out.println(swimmer_item[3]+" : "+gender_id);
            //System.out.println(swimmer_item[5+stroke_id]+" : true\n");
            if(Integer.parseInt(swimmer_item[3])==gender_id && swimmer_item[5+stroke_id].equals("true") && swimmer_item[11+length_id].equals("true"))
            {
                loaded_Swimmers_List.add(Integer.toString(index)); // loaded swimmer list index from Swimmers_List
                loaded_Swimmers_List_model.add(loaded_Swimmers_List_model.size(), swimmer_item[1].charAt(0)+ "."+swimmer_item[0]);       
            }
            index+=1;
        }
        if(loaded_Swimmers_List.size()<=0)
        {
            Message_Box.errorBox("There is no swimmers to swim for this requirements.", "No Swimmer", this);
        }
    }
